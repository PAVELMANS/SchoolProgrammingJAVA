    private void formMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseReleased
        int x1 = evt.getX() - 100;
        int y1 = evt.getY() - 100;
        
        values.push(new Point2D.Double(aMin, bMin));
        values.push(new Point2D.Double(aMax, bMax));
        
        if ((x1 - x) > (y1 - y)) y1 = y + (x1 - x);
        else x1 = x + (y1 - y);
        
        aMax = aMin + x1 * interval;
        bMax = bMin + y1 * interval;
        aMin = aMin + x * interval;
        bMin = bMin + y * interval;
         
        jTextField1.setText(Double.toString(aMin));
        jTextField2.setText(Double.toString(aMax));
        jTextField3.setText(Double.toString(bMin));
        jTextField4.setText(Double.toString(bMax));
        
        draw();
    }//GEN-LAST:event_formMouseReleased

    private void formMouseWheelMoved(java.awt.event.MouseWheelEvent evt) {//GEN-FIRST:event_formMouseWheelMoved
        // TODO add your handling code here:
        Point2D.Double min, max;
        max = values.pop();
        min = values.pop();
        aMin = min.x; bMin = min.y;
        aMax = max.x; bMax = max.y;
        jTextField1.setText(Double.toString(aMin));
        jTextField2.setText(Double.toString(aMax));
        jTextField3.setText(Double.toString(bMin));
        jTextField4.setText(Double.toString(bMax));
        draw();
    }//GEN-LAST:event_formMouseWheelMoved

