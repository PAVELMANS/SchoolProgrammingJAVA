    double aMax, aMin, bMax, bMin, interval;
    int x, y;
    Stack<Point2D.Double> values = new Stack<>();

    private void getCorrectSize() {
        double aDif = aMax - aMin, bDif = bMax - bMin;
        if (aDif > bDif) {
            aMax = aMin + bDif;
            jTextField2.setText(Double.toString(aMax));
        } else {
            bMax = bMin + aDif;
            jTextField4.setText(Double.toString(bMax));
        }
    }

    private int getColourByI(int i) {
        return (255 << 24) | (i * 8 << 16) | (i << 8) | i;
    }

