        at1.rotate(Math.PI + ((Math.PI / 6) * Double.parseDouble(hour) + ((Math.PI / (6 * 60)) * Double.parseDouble(min)) + Math.PI / (6 * 60 * 60)) * Double.parseDouble(sec));
        g2 = (Graphics2D)jPanel2.getGraphics();
        g2.setTransform(at1);
        
        //varbūt ielikt 30*60 iekavās
        at2.rotate(Math.PI + ((Math.PI/6)*Double.parseDouble(min)) + ((Math.PI/30*60)*Double.parseDouble(sec)));
        g3 = (Graphics2D)jPanel3.getGraphics();
        g3.setTransform(at2);
        
       
