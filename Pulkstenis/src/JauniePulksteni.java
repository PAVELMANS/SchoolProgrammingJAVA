        g1.drawImage(img, null, null);

        at1.rotate(Math.PI / (60 * 60 * 30));
        g2 = (Graphics2D) jPanel2.getGraphics();
        g2.setTransform(at1);
        g2.drawLine(0, 0, 0, 89);

        at2.rotate(Math.PI / (30 * 60));
        g3 = (Graphics2D) jPanel3.getGraphics();
        g3.setTransform(at2);
