    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
        // TODO add your handling code here:
        //jTextField1.setText("");
        text.setText("REZULTÄ€TS");
        num2 = Integer.parseInt(jTextField1.getText());
        switch (func) {
            case '+':
                rez = num1 + num2;
                break;
            case '-':
                rez = num1 - num2;
                break;
            case '*':
                rez = num1 * num2;
                break;
            case '/':
                if (num2 == 0){
                    jTextField1.setText("YOU CANNOT DIVIDE BY ZERO!");
                } else {
                    rez = num1 / num2;
                }
                break;
        }
        jTextField1.setText(rez+"");
    }//GEN-LAST:event_equalActionPerformed

